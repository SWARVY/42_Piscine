int	ft_is_prime(int nb)
{
	int	cnt;

	cnt = 2;
	if (nb < 2)
		return (0);
	while (cnt <= nb / cnt)
	{
		if (nb % cnt == 0)
			return (0);
		cnt += 1;
	}
	return (1);
}

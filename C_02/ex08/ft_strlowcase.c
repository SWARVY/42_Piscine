char	*ft_strlowcase(char *str)
{
	int	cnt;

	cnt = 0;
	while (str[cnt] != '\0')
	{
		if (str[cnt] >= 65 && str[cnt] <= 90)
			str[cnt] += 32;
		cnt += 1;
	}
	return (str);
}
